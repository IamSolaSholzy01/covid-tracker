function sort(arr) {
  return arr.sort(function(a, b) {
    var textA = a.state.toUpperCase();
    var textB = b.state.toUpperCase();
    return (textA < textB) ? -1 : (textA > textB) ? 1 : 0;
  });
}

window.addEventListener('load', ()=> {
  loadNumbers(sort(helplinesObject));
}, false);

function loadNumbers(sortedArr) {
  let markup = "";
  for (var i of sortedArr) {
    markup += `<div class="col-12 col-md-4" id="number">` +
      `<h2>${i.state}</h2>` +
      `<div class='row'>`;
    for (let j  of i.number) {
      markup += `<p class="col-6">` +
        `<a href="tel:${j}">${j}</a>` +
      `</p>`;
    }
    markup += "</div></div>"
  }
  document.querySelector('.numbers-category').innerHTML = markup;
}
      
function edit(x) {
  loadNumbers(sort(helplinesObject.filter(i => i.state.toUpperCase().startsWith(x.value.toUpperCase()))))
}
